const grid = newGrid();

const copyButton = document.getElementById("copy");

copyButton.addEventListener("click", (event) => {
    navigator.clipboard.writeText(grid.toString());
})

const drawGrid = (grid) => {
    for (let rowIndex = 0; rowIndex < grid.length; rowIndex++) {
        const row = grid[rowIndex];
        const rowElement = addRow();
        for (let cellIndex = 0; cellIndex < row.length; cellIndex++){
            const cell = row[cellIndex];
            addCell(rowElement, rowIndex, cellIndex, cell)
        }
    }
}

const addRow = () => {
    const element = document.createElement("div");
    element.setAttribute("class", "row");
    content.appendChild(element);
    return element;
};

const addCell = (rowElement, rowIndex, cellIndex, initialValue) => {
    const element = document.createElement("div");
    element.setAttribute("class", "cell");
    element.innerText = cellValueToString(initialValue);
    element.addEventListener("click", (event) => {
        const currentCellValue = grid[rowIndex][cellIndex];
        const newCellValue = currentCellValue === 0 ? 1 : 0;
        grid[rowIndex][cellIndex] = newCellValue;
        event.target.innerText = cellValueToString(newCellValue);
    })
    rowElement.appendChild(element);
}

const cellValueToString = (cellValue) => {
    if (cellValue === 0) {
        return "⬜";
    }
    return "⬛";
}

drawGrid(grid)

